package org.example.metro.model;

/**
 * Модель линии метрополитена.
 *
 * <p>Содержит номер линии, название и цвет линии.
 */
public class Line {

    /** Номер линии метро */
    public int number;

    /** Название линии метро */
    public String name;

    /** Цвет линии метро (например "red") */
    public String color;
}
